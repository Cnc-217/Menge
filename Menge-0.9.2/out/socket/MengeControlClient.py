#coding:utf-8
import socket
import json


def service(jsonData):
    #socket初始化、发送json
    client = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    #serverIp = "10.128.232.195" #服务器
    serverIp = "10.28.195.233" #本机
    serverPort = 12660
    try:
        client.connect((serverIp, serverPort))
    except socket.error:
        print('fail to setup socket connection')
        return
    print('connection ok')

    #发送数据，发送第一次：数据大小 第二次：数据
    messageSend = json.dumps(jsonData)
    client.sendall(str(len(messageSend.encode("utf8"))).encode("utf8"))  # 发送服务端发送给客户端数据的长度
    client.recv(1024).decode("utf8")#接收确认信息
    client.sendall(messageSend.encode("utf8"))

    #1 接收数据：先接收即将发送过来的数据大小,然后循环接收大数据
    data_size = client.recv(1024).decode("utf8")
    receiveSignal = "ok"
    client.sendall(receiveSignal.encode("utf8"))
    recevied_size = 0  # 接收客户端发来数据的计算器
    recevied_data = ''  # 客户端每次发来内容的计数器
    while recevied_size < int(data_size):  # 当接收的数据大小 小于 服务端发来的数据
        dataRecived = client.recv(1024).decode("utf8")
        recevied_size += len(dataRecived)  # 每次收到的服务端的数据有可能小于1024，所以必须用len判断
        recevied_data += dataRecived

    #1 数据转化为json
    jsonRecive = json.loads(recevied_data)

    #2 处理json中的data信息
    if(jsonData["command"]=="Evacuate"):
        # Info:文字
        print(jsonRecive["Info"])
        client.close()
        return
    elif(jsonData["command"]=="FlowScene"):
        #Info:文字
        print(jsonRecive["Info"])
        #Flow:人流信息，转换为list后可以当作list操作，尺寸为1*36
        listFlow = list(jsonRecive["FlowData"])
        #Matrix:矩阵信息，转换为list后可以当作list操作。尺寸为36*36
        listMatrix = list(jsonRecive["Matrix"])

        client.close()
        return listFlow,listMatrix
    elif (jsonData["command"] == "MatrixModify"):
        # Info:文字
        print(jsonRecive["Info"])
        client.close()
        return
    elif (jsonData["command"] == "BusinessScene"):
        # Info:文字
        print(jsonRecive["Info"])
        # Flow:人流信息，转换为list后可以当作list操作，尺寸为1*36
        listFlow = list(jsonRecive["BusinessScene"])
        # Matrix:矩阵信息，转换为list后可以当作list操作。尺寸为36*36
        listMatrix = list(jsonRecive["Matrix"])

        client.close()
        return listFlow, listMatrix

def matrixToStr(matrix):
    #将矩阵转换为为字符串
    matrixStr = ""
    rowNum = len(matrix)
    columnNUm = len(matrix[0])
    sumNum = 0
    for i in matrix:
        for j in i:
            matrixStr = matrixStr + str(j) + " "
            sumNum = sumNum + 1
    if (sumNum != rowNum ** 2 or rowNum != columnNUm):
        print("error!!!")
        exit()
    return matrixStr


